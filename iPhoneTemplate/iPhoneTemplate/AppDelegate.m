- (BOOL)application:(UIApplication *)application didFinishLaunchingWithOptions:(NSDictionary *)launchOptions
{
    application.idleTimerDisabled = YES;// avoid sleep mode
    
    self.window = [[UIWindow alloc] initWithFrame:[[UIScreen mainScreen] bounds]];
    self.viewController = [[ViewController alloc] initWithNibName:@"ViewController" bundle:nil];
    
    _navigationController = [[UINavigationController alloc]initWithRootViewController:_viewController];
    
    self.window.rootViewController = self.navigationController;
    [self.window makeKeyAndVisible];
    
    [[NSUserDefaults standardUserDefaults]setObject:WEB_URL forKey:@"WEB_URL"];
    return YES;
}

